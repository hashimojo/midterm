function plus(value1,value2){
    let result;
    result = value1 + value2;
    return result;
}

function minus(value1,value2){
    let result;
    result = value1 - value2;
    return result;
}

function times(value1,value2){
    let result;
    result = value1 * value2;
    return result;
}

function div(value1,value2){
    let result;
    result = value1 / value2;
    return result;
}

function mod(value1,value2){
    let result;
    result = value1 % value2;
    return result;  
}
